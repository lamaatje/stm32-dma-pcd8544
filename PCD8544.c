static uint8_t spiTransfering=0;
static uint8_t FrameBuf[6][84];

/**
 * Write commands to PCD8544 chip
 * @param data pointer to buffer with commands
 * @param num number of commands
 */
static int32_t write_Commands(uint8_t *data, uint16_t num)
{
  int32_t retval=1;

  if(!spiTransfering)
  {
	HAL_GPIO_WritePin(LCD_CE_PORT, LCD_CE_PIN, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(LCD_DC_PORT, LCD_DC_PIN, GPIO_PIN_RESET);
	spiTransfering=1;
	HAL_SPI_Transmit_DMA(&LCD_SPI_INTERFACE, data, num);
	retval=0;
  }

  return retval;
}


/**
 * Get pointer to FB
 */
uint8_t *LCD_GetFB()
{
	return &FrameBuf[0][0];
}

/**
 * Write frame buffer to PCD8544 chip
 */
int32_t LCD_WriteFB()
{
  int32_t retval=1;

  if(!spiTransfering)
  {
    HAL_GPIO_WritePin(LCD_CE_PORT, LCD_CE_PIN, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(LCD_DC_PORT, LCD_DC_PIN, GPIO_PIN_SET);
	spiTransfering=1;
	HAL_SPI_Transmit_DMA(&LCD_SPI_INTERFACE, &FrameBuf[0][0], LCD_FB_SIZE);
	retval=0;
  }

  return retval;
}

/**
 * Check if a Tfer to PCD8544 is ongoing
 */
int32_t LCD_Busy()
{
	return spiTransfering==1;
}




/**
 * Write a string to a line in the FB, everything passed the end of the line is not written.
 * for 8x6 fonts Y 0..5; X 0..13
 * @param PosX X starting character position on the line
 * @param PosY Y line number
 * @param str  string to write.
 */
void LCD_WriteLine(uint8_t PosX, uint8_t PosY, char * str) {
  uint8_t line, ch;
  uint8_t *bpos;

  while(* str && PosX<14)
  {
	{
      ch=*str-32;
      bpos=&FrameBuf[0][0]+PosY*84+6*PosX;
	  for (line=0; line<6; line++)
	  {
	    *bpos=font6x8[ch][line];
	    bpos++;
	  }
	}
    str ++;
    PosX++;
  }
}


/**
 * Initialize LCD.
 */
void LCD_Init(void) {
  uint8_t cmds[]={0x21,0xD0,0x20,0x0C,0x40,0x80};

  /*
  0x21 H=1 extended instruction set, PD=0 chip active, V=0 horizontal addressing
  0xD0 set Vop 1010000 sets contrast
  0x20 set H=0 basic instruction set normal mode
  0x0C set D=1, E=0
  0x40 set Yxount
  0x80 set Xcount
*/

  HAL_GPIO_WritePin(LCD_RESET_PORT, LCD_RESET_PIN, GPIO_PIN_RESET);
  HAL_Delay(1);
  HAL_GPIO_WritePin(LCD_RESET_PORT, LCD_RESET_PIN, GPIO_PIN_SET);

  write_Commands(cmds,sizeof(cmds));
  memset( &FrameBuf[0][0],0x00,LCD_FB_SIZE);

  //synchronize
  while(LCD_Busy());

  //frame buffer initialized with zero's that clear SRAM of the PCD8544
  LCD_WriteFB();

  //synchronize
  while(LCD_Busy());

}


/**
 * HAL SPI transfer complete callback
 * @param hspi HAL SPI handler.
 */
void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi)
{
	HAL_GPIO_WritePin(LCD_CE_PORT, LCD_CE_PIN, GPIO_PIN_SET);
	spiTransfering=0;
}

